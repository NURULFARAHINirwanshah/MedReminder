package com.psm.medreminder;

public class MedicineData {

    public String med_mid;
    public String med_sname;
    public String med_dosage;
    public String med_indication;
    public String med_date;
    public String med_schedule;


    public MedicineData(){

    }

    public String getMed_mid() {
        return med_mid;
    }

    public void setMed_mid(String med_mid) {
        this.med_mid = med_mid;
    }

    public String getMed_sname() {
        return med_sname;
    }

    public void setMed_sname(String med_sname) {
        this.med_sname = med_sname;
    }

    public String getMed_dosage() {
        return med_dosage;
    }

    public void setMed_dosage(String med_dosage) {
        this.med_dosage = med_dosage;
    }

    public String getMed_indication() {
        return med_indication;
    }

    public void setMed_indication(String med_indication) {
        this.med_indication = med_indication;
    }

    public String getMed_date() {
        return med_date;
    }

    public void setMed_date(String med_date) {
        this.med_date = med_date;
    }

    public String getMed_schedule() {
        return med_schedule;
    }

    public void setMed_schedule(String med_schedule) {
        this.med_schedule = med_schedule;
    }
}
